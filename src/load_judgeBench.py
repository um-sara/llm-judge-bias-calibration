"""
Download JudgeBench from HuggingFace and normalize to MT-Bench schema.

JudgeBench (Tan et al. 2024, arXiv:2410.12784) — ScalerLab/JudgeBench
Two splits:
    gpt    — 350 pairs, responses generated by GPT-4o
    claude — 270 pairs, responses generated by Claude-3.5-Sonnet
    total  — 620 pairs

Labels are binary: "A>B" or "B>A" (no ties).

Normalized output schema (matches mt_bench.csv):
    question_id      — pair_id from JudgeBench
    source           — original source dataset field (kept for filtering)
    model_a          — "judgebench_A" (placeholder; response identity not exposed)
    model_b          — "judgebench_B"
    winner           — "model_a" | "model_b"  (mapped from A>B / B>A)
    judge            — "human"
    turn             — 1 (all single-turn)
    conversation_a   — [{"role": "user", ...}, {"role": "assistant", ...}]
    conversation_b   — [{"role": "user", ...}, {"role": "assistant", ...}]
    split            — "gpt" | "claude" (which JudgeBench split)
    
"""

import argparse
import json
import os

import pandas as pd
from datasets import load_dataset


SPLITS = ["gpt", "claude"]


def load_and_normalize(split: str) -> pd.DataFrame:
    print(f"  Downloading split '{split}'...")
    ds = load_dataset("ScalerLab/JudgeBench", split=split)
    df = pd.DataFrame(ds)
    print(f"  {len(df)} rows, columns: {list(df.columns)}")

    # Map label to winner
    def map_label(label: str) -> str:
        if label == "A>B":
            return "model_a"
        elif label == "B>A":
            return "model_b"
        else:
            raise ValueError(f"Unexpected JudgeBench label: '{label}'")

    # Build conversation dicts in MT-Bench format.
    # ensure_ascii=False keeps supplementary-plane characters (emoji, etc.)
    # as literal codepoints. The default escapes them to surrogate-pair \uXXXX
    # sequences, which ast.literal_eval (used downstream in parse_conversation)
    # then decodes as lone surrogates — breaking UTF-8 encoding in httpx.
    def make_conversation(question: str, response: str) -> str:
        conv = [
            {"role": "user",      "content": question},
            {"role": "assistant", "content": response},
        ]
        return json.dumps(conv, ensure_ascii=False)

    normalized = pd.DataFrame({
        "question_id":    df["pair_id"],
        "source":         df["source"],
        "model_a":        "judgebench_A",
        "model_b":        "judgebench_B",
        "winner":         df["label"].apply(map_label),
        "judge":          "human",
        "turn":           1,
        "conversation_a": df.apply(
            lambda r: make_conversation(r["question"], r["response_A"]), axis=1
        ),
        "conversation_b": df.apply(
            lambda r: make_conversation(r["question"], r["response_B"]), axis=1
        ),
        "split":          split,
    })

    return normalized


def main():
    parser = argparse.ArgumentParser(
        description="Download JudgeBench from HuggingFace and normalize to MT-Bench schema."
    )
    parser.add_argument(
        "--split",
        choices=SPLITS,
        default=None,
        help="Which split to load (default: both combined)",
    )
    args = parser.parse_args()

    os.makedirs("data", exist_ok=True)

    splits_to_load = [args.split] if args.split else SPLITS

    print("Loading JudgeBench from HuggingFace (ScalerLab/JudgeBench)...")
    parts = []
    for split in splits_to_load:
        parts.append(load_and_normalize(split))

    df = pd.concat(parts, ignore_index=True)

    # Sanity checks
    assert df["winner"].isin(["model_a", "model_b"]).all(), "Unexpected winner values"
    assert (df["turn"] == 1).all(), "Expected all turn=1"
    assert df["question_id"].nunique() == len(df), "Duplicate pair_ids across splits"

    print(f"\nCombined: {len(df)} rows")
    print(f"  splits:  {dict(df['split'].value_counts())}")
    print(f"  winner:  {dict(df['winner'].value_counts())}")
    if "source" in df.columns:
        print(f"  sources: {dict(df['source'].value_counts())}")

    out_path = "data/judgebench.csv"
    df.to_csv(out_path, index=False)
    print(f"\nSaved to {out_path}")


if __name__ == "__main__":
    main()
